export default class Calculator{

    constructor(){

    }

    public sum (a:number, b:number):number{
        return a + b;
    }

    public sub (a:number, b:number):number{
        return a - b;
    }

    public mult (a:number, b:number):number{
        return a * b;
    }

    public div (a:number, b:number):number{
        return a / b;
    }
}